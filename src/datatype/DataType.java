package datatype;

public class DataType {
    public static void main(String[] args) {
        //byte,short,int,long-for numerical values
        //where a is variable and 10 is stored value

        int a = 10;
        int b = 20;
        int result = a + b;
        // result is also variable
        System.out.println(result);
        // double and float for decimal value to store float use f at end
        // double is more precise than float
        double d = 5.54;
        float e = 10.5f;
        double result1 = d + e;
        System.out.println(result1);

        // character - for single character use char
        char c = 'H';
        char H = 10;
        System.out.println(c);
        //boolean - for true or false
        boolean myResult = true;
        System.out.println(myResult);
        // variable is always small and inner word is starts with Capital and always one word
        boolean yourResult = false;
        System.out.println(yourResult);
        // String - for word or statement always ""
        String name = "Prime Testing";
        System.out.println(name);
        String myName = "Ashish Tiwari";
        System.out.println(myName);
        //out is function print is method
        System.out.println("Ashish Is Idiot" + (a+b));



    }

}







